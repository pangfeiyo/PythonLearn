'''
有一个长阶梯
若每步上2 阶，最后剩1 阶；若每步上3 阶，最后剩2 阶
若每步上5 阶，最后剩4 阶；若每步上6 阶，最后剩5 阶
只有每步上7 阶，最后刚好一阶也不剩
'''

x = 7
i = 1
flag = 0

while i<=100:
    if(x % 2 == 1 ) and (x % 3 == 2) and (x % 5 == 4) and (x % 6 == 5):
        flag = 1
    else:
        x = 7 *(i + 1 )  #根据题意，X一定是7的整倍数，所以每次*7

    i += 1

if flag ==1:
    print("阶梯数是：",x)
else:
    print("在程序限定范围内找不到答案！")
