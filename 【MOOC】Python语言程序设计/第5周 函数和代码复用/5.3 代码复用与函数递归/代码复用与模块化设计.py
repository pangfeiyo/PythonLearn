# 模块化设计
# 分而治之
'''
  - 通过函数或对象封闭装程序划分为模块及模块间的表达
  - 具体包括：主程序、子程序和子程序间关系
  - 分而治之：一种分而治之、分层抽象、体系化的设计思想
'''


